package flowershop.pojo;

public class FlowerMakings {

	 private String fmid;
	 private String fmName;
	 private String fmImage;
	 private String fmImageBitmap;
	public String getFmid() {
		return fmid;
	}
	public void setFmid(String fmid) {
		this.fmid = fmid;
	}
	public String getFmName() {
		return fmName;
	}
	public void setFmName(String fmName) {
		this.fmName = fmName;
	}
	public String getFmImage() {
		return fmImage;
	}
	public void setFmImage(String fmImage) {
		this.fmImage = fmImage;
	}
	public String getFmImageBitmap() {
		return fmImageBitmap;
	}
	public void setFmImageBitmap(String fmImageBitmap) {
		this.fmImageBitmap = fmImageBitmap;
	}
	 
}
